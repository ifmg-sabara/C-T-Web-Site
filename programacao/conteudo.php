<h2 id="abertura">Abertura</h2>

<p><strong>Data</strong>: Quarta-feira – 25 de outubro <br>
<strong>Horário</strong>: 18:30 às 21:00</p>

<h3 id="detalhamento">Detalhamento</h3>

<ul>
<li>Cerimônia de Abertura: O início da V Semana de Ciência e Tecnologia está prevista para ocorrer na Escola Estadual Bilu de Figueiredo e contará com a participação dos seus estudantes, dos estudantes do IFMG campus Sabará e membros da comunidade externa.</li>
<li>Palestra abordando o tema da Semana Nacional de Ciência e Tecnologia “A matemática está em tudo”.</li>
<li>IFMG/DANÇA COM LED: A dança é uma apresentação cultural que tem como objetivo apresentar o resultado do trabalho dos estudantes na construção das roupas que utilizavam circuitos eletrônicos e fitas de LEDs, os quais, são acionados com o ritmo da música.</li>
<li>Apresentação cultural dos alunos da Escola Estadual Bilu de Figueiredo: Apresentação cultural da escola estadual.</li>
<li>Apresentação de Pitchs: Os Pitchs são filmes curtos que demonstram de forma sucinta os projetos tecnológicos desenvolvidos pelos estudantes, sendo uma prévia do que será apresentado durante a Feira Tecnológica e a Mostra de Empreendedorismo.</li>
<li>Apresentação de trabalhos científicos: Os bolsistas de iniciação científica irão apresentar os resultados dos trabalhos por meio de exposição de banners. Os mesmos serão arguidos por avaliadores convidados.</li>
<li>Entrega do prêmio para a Logomarca selecionada que compôs o evento, de acordo com o edital interno do IFMG campus Sabará.</li>
</ul>



