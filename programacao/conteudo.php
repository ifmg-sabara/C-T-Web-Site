<h2 id="mostra-tecnológica">Mostra Tecnológica</h2>

<p><strong>Data</strong>: <br>
<strong>Horário</strong>: 9:00 as 15:00</p>



<h3 id="detalhamento-4">Detalhamento</h3>

<p>A Mostra Tecnológica possibilitará aos alunos dos cursos das instituições participantes apresentarem seus projetos desenvolvidos ao longo do ano sob a orientação de profissionais de diversas áreas. Os projetos demonstrarão os conhecimentos adquiridos pelos alunos nas disciplinas dos cursos que estão matriculados (Eletrônica, Informática, Administração, Processos Gerenciais, Logística, Sistemas de Informação). Os resultados alcançados serão apresentados por meio de exposição de produtos, protótipos ou maquetes. Como estrutura, os projetos da Mostra Tecnológica serão apresentados em estandes que possuem mesas e cadeiras. Os estandes também contaram com televisão LED, que possibilitará aos alunos expor os Pitches dos projetos.</p>



<h2 id="feira-de-empreendedorismo">Feira de empreendedorismo</h2>

<p><strong>Data</strong>: <br>
<strong>Horário</strong>: 9:00 as 15:00</p>



<h3 id="detalhamento-5">Detalhamento</h3>

<p>Apresentação dos escopos de empresas desenvolvidas por estudantes. Cursos envolvidos: Bacharelado em Sistemas de Informação, Tecnologia em Processos Gerenciais e Tecnologia em Logística. Tem como objetivo geral desenvolver o espírito empreendedor dos alunos a partir de uma vivência real de se criar e colocar em prática a abertura de um negócio. Mais especificamente, essa feira incentivou os estudantes na identificação de problemas e propostas de resolução a partir da criação de uma empresa.</p>



