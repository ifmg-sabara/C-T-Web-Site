<p>As inscrições de propostas de oficinas e cursos ocorrerão por meio de edital do IFMG. Essas propostas serão avaliadas pela comissão organizadora. A ministração das oficinas e dos cursos serão abertas para alunos e servidores das instituições envolvidas e para a comunidade metropolitana. Membros da comunidade também poderão ofertar oficinas durante a semana de C&amp;T, propiciando assim aos estudantes e servidores do IFMG a absorção de conhecimento e culturas regionais.</p>

