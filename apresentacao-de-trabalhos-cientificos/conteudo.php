<p>Os alunos de diversos cursos do IFMG campus Sabará realizam continuamente projetos de pesquisa em diversas áreas do conhecimento. A semana de C&amp;T é a oportunidade desses alunos apresentarem os resultados de suas pesquisas para a comunidade acadêmica e para a comunidade regional. Durante a abertura do evento é separado um local para a exposição de banners dos projetos. Professores conceituados serão convidados para a avaliação dos trabalhos.</p>

